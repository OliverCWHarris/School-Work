class animal():
    def __init__(self, name, age, blood, size, sound):

        self.name=name
        self.age=age
        self.blood=blood
        self.size=size
        self.sound=sound

    def get_name(self):
        return self.name
        
    def get_age(self):
        return self.age
        
    def get_blood_type(self):
        return self.blood
        
    def get_size(self):
        return self.size
        
    def get_sound(self):
        return self.sound